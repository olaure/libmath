#include "ft_math.h"

double	ft_sqrt(double n)
{
	double	r;
	double	d;

	if (n == 0.0)
		return (0.0);
	r = n / 2;
	while (n / r != r)
	{
		d = (n - r * r) / (2 * r);
		if (d && ABS(d) < 0.000000000000001)
			break ;
		r += d;
	}
	return (r);
}
